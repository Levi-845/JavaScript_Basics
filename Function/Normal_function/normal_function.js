function fullName(firstName, LastName) {
    document.write("Hi! " + firstName + " " + LastName);
    document.write("<br>");
};
fullName('Ahmed', 'Naviwala');
// OutPut :- Hi! Ahmed Naviwala


function helloWorld(word1, word2) {
    document.write("<br>");
    document.write(word1 + word2);
};
helloWorld("Hello ","World");
// OutPut :- Hello World
